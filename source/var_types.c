#include<stdio.h>

int main(){
	int weight = 95;
	float height = 185.5;
	double salary = 99999.99;
	char initial = 'D';
	char first_name[] = "Daniel";
	char last_name[] = "Szabo";

	printf("I am %d kg-s heavy!\n",weight);
	printf("My initial is %c\n",initial);
	printf("My height is %f\n",height);
	printf("My salary is %lf\n",salary);
	printf("My first name is %s\n",first_name);
	printf("My last name is %s\n",last_name);
}
